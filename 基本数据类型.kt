package FirstDay



fun main(args:Array<String>){
    var a:Int = 20
    print(a)

    var b:Byte = 2
    print(b)

    val c:Char = 'a'
    print(c)

    val s:Short = 2000
    print(s)

    var f:Float = 5.122F
    print(f)

    var l:Long = 21651L
    print(l)

    var d:Double = 516516.0
    print(d)

    var y:Boolean = true
    print(y)

    var x = "String类型"
    print(x)









}
