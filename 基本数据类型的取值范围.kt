package FirstDay


//几个常见数据类型的最大值和最小值
fun main(args:Array<String >){
    val maxI :Int = Int.MAX_VALUE
    val minI :Int = Int.MIN_VALUE

    print(maxI)
    print(minI)


    var maxB :Byte = Byte.MAX_VALUE
    var minB :Byte = Byte.MIN_VALUE
    print(maxB)
    print(minB)

    var maxS :Short = Short.MAX_VALUE
    var minS :Short = Short.MIN_VALUE
    print(maxS)
    print(minS)

    var maxL :Long = Long.MAX_VALUE
    var minL :Long = Long.MIN_VALUE
    print(maxL)
    print(minL)

    val maxD :Double = Double.MAX_VALUE
    val minD :Double = Double.MIN_VALUE
    print(maxD)
    print(minD)


    val maxF : Long = Long.MAX_VALUE
    val minF : Long = Long.MIN_VALUE
    print(maxF)
    print(minF)

}